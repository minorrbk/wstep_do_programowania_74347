
print (int(3.0))
#instrukcja int(3.0) wydrukuje nam liczbę całkowitą 3

print (float(3))
#instrukcja float(3) wydrukuje nam liczbę zmiennoprzecinkową 3.0

print (float("3"))
#instrukcja float("3") wydrukuje nam liczbę zmiennoprzecinkową 3.0, jednak będzie ona w formacie tekstowym

print (str(12.4))
#instrukcja str(12.4) wydrukuje nam liczbę 12.4 w formacie tekstowym, narzuconym przez nas z góry jako operator str

print (bool(0))
#instrukcja bool(0) wydrukuje nam napis false, ponieważ wartość w nawiasie wynosi 0